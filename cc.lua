    math.randomseed(tostring(os.time()):reverse():sub(1, 7))
    if (radua == 1) then
        wrk.headers["User-Agent"] = ua[math.random(1,74)]
    else
        wrk.headers["User-Agent"] = "Powered by WRK"
    end  
    --随机ua传递结束
    
    if (radmethod == 1) then
        wrk.method = method[math.random(1,8)]
    else
        wrk.method = methoddef
    end 
    --随机请求方法结束
    
    if (radque==1)then
        uid = math.random(1, quemax)
        path = uri .. uid ..uriend
    end  
    --随机查询结束
    --返回数据开始
   return wrk.format(wrk.method,path,wrk.headers,null)
end
